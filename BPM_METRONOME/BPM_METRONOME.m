%% click callback to measure BPM

function callBack(hObject, ~)

hObject.UserData.recent = [toc(hObject.UserData.t0), hObject.UserData.recent(1:end-1)];
avg_toc = mean(hObject.UserData.recent, 'omitnan');
if hObject.Children(1).Value
    hObject.Children(end).Children(end).String = num2str(round(1/avg_toc*60));
else
    hObject.Children(end).Children(end).String = num2str(round(1/hObject.UserData.recent(1)*60));
end
hObject.UserData.t0 = tic;
end

%% metronome

function play_metronome(hObject, ~)

bpm = str2double(hObject.Parent.Children(end-1).String);
interval = 60 / bpm;
Fs = length(tic_sound())*10;

player = audioplayer(tic_sound(), Fs);

persistent metronomeTimer
if isempty(metronomeTimer)
    metronomeTimer = timer('TimerFcn', {@playClick, player}, ...
        'StartDelay', 0, 'Period', interval, ...
        'ExecutionMode', 'fixedRate');
end

if strcmp(hObject.Style, 'pushbutton')
    if hObject.UserData
        hObject.UserData = 0;
        stop(metronomeTimer)
    else
        hObject.UserData = 1;
        start(metronomeTimer)
    end
elseif strcmp(hObject.Style, 'edit')
    if hObject.Parent.Children(end-2).UserData
        stop(metronomeTimer)
    end
    metronomeTimer.Period = interval;
    if hObject.Parent.Children(end-2).UserData
        start(metronomeTimer)
    end
end


end

%% player

function playClick(~, ~, player)
    play(player);
end

